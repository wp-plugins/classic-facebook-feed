    <h2>Classic Facebook Feeds</h2>
    <p>
    	Classic Facebook Feed is a plugin for display facebook feed on your blog or site. Here you can Get feed fron any Facebook Page. This plugin use Facebook Opengraph API so its highly customizable and you can style the feeds the way you like.
    </p>
    

	<h4>Features :</h4>	
	<ol>
		<li>Easily add Facebook Feed using the shortcode</li>
		<li>Display Facebook Page Feed</li>
		<li>Set Limit Number of Posts</li>
		<li>Linking HashTags</li>
		<li>Text / Font Size customization using CSS</li>
		<li>Support for Custom CSS</li>
	</ol>
	
